namespace Lilypad.Text; 

/// <summary>
/// Preset JSON text colors.
/// </summary>
public enum TextColor {
    /// <summary>
    /// Default value for signs and books. Hex value: <c>#000000</c>
    /// </summary>
    Black,
    
    /// <summary>
    /// Hex value: <c>#0000AA</c>
    /// </summary>
    DarkBlue,
    
    /// <summary>
    /// Hex value: <c>#00AA00</c>
    /// </summary>
    DarkGreen,
    
    /// <summary>
    /// Hex value: <c>#00AAAA</c>
    /// </summary>
    DarkAqua,
    
    /// <summary>
    /// Hex value: <c>#AA0000</c>
    /// </summary>
    DarkRed,
    
    /// <summary>
    /// Hex value: <c>#AA00AA</c>
    /// </summary>
    DarkPurple,
    
    /// <summary>
    /// Hex value: <c>#FFAA00</c>
    /// </summary>
    Gold,
    
    /// <summary>
    /// Hex value: <c>#555555</c>
    /// </summary>
    Gray,
    
    /// <summary>
    /// Default color for item lore. Hex value: <c>#555555</c>
    /// </summary>
    DarkGray,
    
    /// <summary>
    /// Hex value: <c>#5555FF</c>
    /// </summary>
    Blue,
    
    /// <summary>
    /// Hex value: <c>#55FF55</c>
    /// </summary>
    Green,
    
    /// <summary>
    /// Hex value: <c>#55FFFF</c>
    /// </summary>
    Aqua,
    
    /// <summary>
    /// Hex value: <c>#FF5555</c>
    /// </summary>
    Red,
    
    /// <summary>
    /// Hex value: <c>#FF55FF</c>
    /// </summary>
    LightPurple,
    
    /// <summary>
    /// Hex value: <c>#FFFF55</c>
    /// </summary>
    Yellow,
    
    /// <summary>
    /// The default color for chat messages. Hex value: <c>#FFFFFF</c>
    /// </summary>
    White
}